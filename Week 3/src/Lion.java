
public class Lion implements Animal {

	@Override
	public void Eat() {
		System.out.println("Lion eats meat");

	}

	@Override
	public void Move() {
		System.out.println("Lion moves fast");

	}

	@Override
	public void Hunt() {
		System.out.println("Lion hunts other animals");

	}

	@Override
	public void MakeSound() {
		System.out.println("Lion roars");

	}

}
