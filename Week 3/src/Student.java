
public class Student {
	
	String Name;
	int Age;
	int rollnu;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getAge() {
		return Age;
	}
	public void setAge(int age) {
		Age = age;
	}
	public int getRollnu() {
		return rollnu;
	}
	public void setRollnu(int rollnu) {
		this.rollnu = rollnu;
	}


}
